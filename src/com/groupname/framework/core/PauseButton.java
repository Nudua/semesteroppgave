package com.groupname.framework.core;

/**
 * Represents the different actions a PauseMenu can execute.
 */
public enum PauseButton {

    /**
     * Resumes the game.
     */
    Resume,

    /**
     * Saves the game.
     */
    Save,

    /**
     * Restart the level.
     */
    RestartLevel,

    /**
     * Restart the game.
     */
    RestartGame,

    /**
     * Take you to main menu.
     */
    MainMenu
}
