package com.groupname.framework.graphics.drawing;

/**
 * Orientations to flip sprites.
 */
public enum SpriteFlip {
    /**
     * Don't flip the sprite.
     */
    NONE,
    /**
     * Flip the sprite horizontally along the x-axis.
     */
    HORIZONTAL,
    /**
     * Flip the sprite vertically along the y-axis.
     */
    VERTICAL
}
