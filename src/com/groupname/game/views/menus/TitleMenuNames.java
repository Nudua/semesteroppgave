package com.groupname.game.views.menus;

/**
 * An enum used for navigation on the title menu.
 */
public enum TitleMenuNames {
    /**
     * Used to start a new game.
     */
    NewGame,
    /**
     * Loads and continues the game from file.
     */
    Continue,
    /**
     * Navigate to the level editor.
     */
    Editor,
    /**
     * Navigate to the credits.
     */
    Credits,
    /**
     * Exit the game.
     */
    Exit
}
