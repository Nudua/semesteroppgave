package com.groupname.game.levels.core;

/**
 * Enum list representing the states of the level.
 */
public enum LevelState {

    /**
     * Loading level.
     */
    LOADING,

    /**
     * Playing level.
     */
    PLAYING,

    /**
     * Level completed.
     */
    COMPLETED,

    /**
     * GAME over.
     */
    GAME_OVER
}
